// Declarando uma variável com let
let nome = "João"; // Você pode mudar o valor depois
console.log(nome);
nome = "Edenilson";
console.log(nome);
// Declarando uma variável com const
const idade = 25; // O valor não pode ser alterado
console.log(idade);
// Tentando alterar o valor de uma constante (isso causarará um erro)
// idade = 30; // Descomente esta linha para ver o erro
// Declarando uma variável sem var, let ou const (não recomendado)
cidade = "são Paulo"; // Isso cria uma variável global
console.log(cidade);

//  Tipos de Dados em Variáveis
let numero = 10; // Número inteiro
let numeroDecimal = 10.5; // Número decimal
let texto = "olá mundo"; // String
let booleano = true; // Booleano
let nulo =null; // Nulo

// Lista (Array)
let list = [1,2,3,4,5];
console.log(list);

